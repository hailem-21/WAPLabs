'use strict'


function sum(x)
{
    
    let sum=0;
    
    for(let i=0;i<x.length;i++)
    {
        sum+=x[i];

    }
    return sum;
}


function multiply(x)
{

    let mul=1;
      
    for(let i=0;i<x.length;i++)
    {
        mul*=x[i];

    }
    return mul;
}

